class Geeks {
    static void evenOdd(int a, int b) {
        
        if (a % 2 == 0) {
            System.out.println(a);
        }
        if (b % 2 == 0) {
            System.out.println(b);
        }
        
        if (a % 2 != 0) {
            System.out.println(a);
        }
        if (b % 2 != 0) {
            System.out.println(b);
        }
    }
}